"""E1 - same-date exact-amount candidate generation (NO description gate).

The as-is engine scores 0.00% because tier 1's text gate never opens on
obfuscated attributes and tier 2 structurally excludes same-date pairs. E1
tests the opposite hypothesis: block each B on (amount magnitude, valueDate)
against all A in its partition, and auto-predict only when the block yields a
UNIQUE A - deterministic, no text at all.

Rigor (reviewer fixes 2/3/7):
  * group-safe dev/validation split by matchId (no leakage);
  * precision reported with a Wilson one-sided lower bound, not a point estimate;
  * results stratified by group cardinality (1:1 / 1:N / N:1 / N:M / unmatched)
    so the 1:1 path is never credited with N:M coverage.

Run:  uv run python experiments/benchrec/candidates.py
Emits: data/benchrec/artifacts/e1_candidates.md
"""
from __future__ import annotations

import os
from collections import Counter

from bench_io import load_train, provenance_lines
from bench_stats import (
    Z_95,
    build_scope_index,
    eligible_candidates,
    min_n_for_bound,
    scope_key,
    split_populations,
    wilson_lower_bound,
)

HERE = os.path.dirname(__file__)
DATA = os.path.join(HERE, "..", "..", "data", "benchrec")
TRAIN = os.path.join(DATA, "BenchRec_cash_v1.0_train.csv")
ART = os.path.join(DATA, "artifacts")

STRATA = ("1:1", "1:N", "N:1", "N:M", "unmatched")


def _run(pop, b_card):
    """Unique + mutual-unique rules over one partition; overall + per-stratum stats.

    Candidates obey hard-scope eligibility (currency, account) + opposite-DR/CR
    direction (bench_stats.eligible_candidates), per the research and E0.
    unique         : B has exactly one eligible A in scope.
    mutual-unique  : additionally, that A has exactly one eligible B in scope
                     (reciprocal 1:1) - the true-1:1 auto path.
    """
    a_index = build_scope_index(pop["A"])
    b_index = build_scope_index(pop["B"])
    removed = 0  # same-direction/out-of-scope candidates rejected by eligibility

    cls = Counter()
    # per stratum, per rule: [predicted, correct, matchable, total]
    strat = {s: {"uniq": [0, 0, 0, 0], "mutual": [0, 0, 0, 0]} for s in STRATA}
    agg = {"uniq": [0, 0], "mutual": [0, 0]}   # [predicted, correct] overall
    matchable = fp_unmatched = 0
    for b in pop["B"]:
        s = b_card.get(b.rec_id, "unmatched")
        strat[s]["uniq"][3] += 1
        strat[s]["mutual"][3] += 1
        if b.target:
            matchable += 1
            strat[s]["uniq"][2] += 1
            strat[s]["mutual"][2] += 1
        in_scope = a_index.get(scope_key(b), [])
        cands = eligible_candidates(b, a_index)
        removed += len(in_scope) - len(cands)
        n = len(cands)
        cls["unique" if n == 1 else ("ambiguous" if n > 1 else "impossible")] += 1
        if n == 1:
            a = cands[0]
            corr = bool(b.target) and a.allocation == b.target
            _tally(agg["uniq"], strat[s]["uniq"], corr)
            if not b.target:
                fp_unmatched += 1
            # reciprocal: the sole A must also see exactly one eligible B in scope
            recip = [x for x in b_index.get(scope_key(a), []) if x.direction != a.direction]
            if len(recip) == 1:
                _tally(agg["mutual"], strat[s]["mutual"], corr)
    total = len(pop["B"])
    return {
        "cls": cls, "agg": agg, "matchable": matchable,
        "fp_unmatched": fp_unmatched, "total": total, "strat": strat,
        "removed": removed,
    }


def _tally(agg, strat_rule, correct: bool) -> None:
    agg[0] += 1
    strat_rule[0] += 1
    if correct:
        agg[1] += 1
        strat_rule[1] += 1


def _prec_cell(correct: int, predicted: int) -> str:
    if predicted == 0:
        return "n/a"
    p = correct / predicted
    lb = wilson_lower_bound(correct, predicted, Z_95)
    flag = "PASS" if lb >= 0.998 else "fail"
    return f"{p:.3%} (LB {lb:.3%}, {flag})"


def main() -> None:
    _a, _b, groups = load_train(TRAIN)
    pops, b_card = split_populations(groups, dev_frac=0.7)
    results = {split: _run(pops[split], b_card) for split in ("dev", "val")}

    min_n = min_n_for_bound(0.998, Z_95)
    removed = results["dev"]["removed"] + results["val"]["removed"]
    lines = [
        "# E1 - Same-date exact-amount candidates (no desc gate)",
        "",
        "> Generated by `experiments/benchrec/candidates.py`. Auto-predict only on a UNIQUE",
        "> **eligible** A in scope `(amount, valueDate, currency, account)` with **opposite",
        "> DR/CR** direction; no text signal. Group-safe 70/30 dev/val split by matchId.",
        "> Precision shows the point estimate and a Wilson 95% one-sided lower bound (LB);",
        f"> the 99.8% bar needs **LB >= 99.8%** (with zero errors, only at n >= **{min_n}**).",
        "",
        *provenance_lines({"train": TRAIN}, {"split": "matchId 70/30", "dev_frac": 0.7,
                                             "block": "(amount,valueDate,currency,account)+oppositeDR/CR"}),
        "",
        f"> Eligibility note: scope (currency, account) + opposite-DR/CR removed **{removed}** "
        "candidates across dev+val - a no-op on BenchRec (single USD/ACC#00001, blocks already "
        "single-direction), but encoded for correctness and multi-currency/mixed-direction data.",
        "",
    ]
    for split in ("dev", "val"):
        r = results[split]
        u_pred, u_corr = r["agg"]["uniq"]
        m_pred, m_corr = r["agg"]["mutual"]
        lines += [
            f"## {split.upper()} partition",
            f"- B records: {r['total']:,} (matchable {r['matchable']:,}, "
            f"true-unmatched {r['total'] - r['matchable']:,})",
            f"- **unique** rule: coverage {u_pred / r['total']:.1%} of B, "
            f"recall {u_corr / r['matchable']:.1%} of matchable, "
            f"precision {_prec_cell(u_corr, u_pred)}; FP on true-unmatched {r['fp_unmatched']}",
            f"- **mutual-unique** rule: coverage {m_pred / r['total']:.1%} of B, "
            f"recall {m_corr / r['matchable']:.1%} of matchable, "
            f"precision {_prec_cell(m_corr, m_pred)}",
            "",
            "| cardinality | B (matchable) | unique recall / prec (LB) | mutual recall / prec (LB) |",
            "|---|---|---|---|",
        ]
        for s in STRATA:
            up, uc, um, ut = r["strat"][s]["uniq"]
            mp, mc, _mm, _mt = r["strat"][s]["mutual"]
            u_rec = f"{uc / um:.1%}" if um else "n/a"
            m_rec = f"{mc / um:.1%}" if um else "n/a"
            lines.append(f"| {s} | {ut:,} ({um:,}) | {u_rec} / {_prec_cell(uc, up)} | "
                         f"{m_rec} / {_prec_cell(mc, mp)} |")
        lines.append("")

    dev, val = results["dev"], results["val"]
    dm, vm = dev["agg"]["mutual"], val["agg"]["mutual"]
    d11 = dev["strat"]["1:1"]["mutual"]
    v11 = val["strat"]["1:1"]["mutual"]
    lines += [
        "## E1 conclusion",
        "- The text-free **unique** rule recovers ~**65%** of matchable B (dev+val), up from the "
        "as-is **0.00%** - concentrated almost entirely in the **1:1** stratum. The errors are a "
        "**cardinality** artifact, not a text one: the rule occasionally fires a lone-wrong-A pick "
        "on non-1:1 (mainly N:M) B's.",
        f"- **Reciprocity is not enough on its own.** Overall mutual-unique precision LB is "
        f"**{wilson_lower_bound(dm[1], dm[0]):.3%}** (dev) / **{wilson_lower_bound(vm[1], vm[0]):.3%}** "
        "(val) - better than plain unique but still **below the 99.8% bar**; some N:M B's survive "
        "the reciprocity test and are mispicked.",
        f"- **The 1:1 stratum itself is auto-match-grade:** restricted to true 1:1, mutual-unique "
        f"precision LB is **{wilson_lower_bound(d11[1], d11[0]):.3%}** (dev) / "
        f"**{wilson_lower_bound(v11[1], v11[0]):.3%}** (val) at ~**{d11[1] / d11[2]:.0%}** recall - "
        "both **PASS**. But cardinality is not observable at inference, so this ceiling is only "
        "realizable once non-1:1 B's are correctly handled (E4 N:M) or an abstention signal "
        "identifies them.",
        "- **Implication:** the headline auto-match number is coupled to N:M, exactly as the review "
        "warned - a 1:1 rule alone cannot reach 99.8% *auto* on the full population. E2 (character "
        "features) serves the narrower job of disambiguating genuinely ambiguous 1:1 blocks.",
    ]

    os.makedirs(ART, exist_ok=True)
    out = os.path.join(ART, "e1_candidates.md")
    with open(out, "w") as f:
        f.write("\n".join(lines) + "\n")
    print(f"wrote {out}")
    for split in ("dev", "val"):
        r = results[split]
        up, uc = r["agg"]["uniq"]
        mp, mc = r["agg"]["mutual"]
        print(f"{split}: unique recall={uc / r['matchable']:.1%} "
              f"precLB={wilson_lower_bound(uc, up):.3%} | "
              f"mutual recall={mc / r['matchable']:.1%} precLB={wilson_lower_bound(mc, mp):.3%}")


if __name__ == "__main__":
    main()
