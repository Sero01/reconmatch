"""E0 - BenchRec data profiling (no matching change).

Establishes, on the TRAIN split only, the facts that justify the E1 block key:
population/cardinality shape, allocation reconstructability, direction relation
of true pairs, and - the crux - blocking recall and block-size distribution for
candidate keys, plus the unique/ambiguous/impossible split for 1:1 B records.

Run:  uv run python experiments/benchrec/profile.py
Emits: data/benchrec/artifacts/e0_profile.md
"""
from __future__ import annotations

import os
from collections import Counter, defaultdict

from bench_io import Record, load_train, provenance_lines, reconstruct_alloc

HERE = os.path.dirname(__file__)
DATA = os.path.join(HERE, "..", "..", "data", "benchrec")
TRAIN = os.path.join(DATA, "BenchRec_cash_v1.0_train.csv")
ART = os.path.join(DATA, "artifacts")


def _pct(sizes: list[int], q: float) -> int:
    return sizes[min(len(sizes) - 1, int(q * len(sizes)))]


def _one_to_one(groups) -> list[tuple[Record, Record]]:
    return [(g["B"][0], g["A"][0]) for g in groups.values()
            if len(g["A"]) == 1 and len(g["B"]) == 1]


def _blocking(pairs, a_records, keyfn):
    """Blocking recall + block-size percentiles for a B-side key over all A."""
    index: dict = defaultdict(list)
    for a in a_records:
        index[keyfn(a)].append(a)
    hit, sizes = 0, []
    for b, a in pairs:
        cands = index.get(keyfn(b), [])
        if any(c.rec_id == a.rec_id for c in cands):
            hit += 1
        sizes.append(len(cands))
    sizes.sort()
    return hit / len(pairs), (_pct(sizes, .5), _pct(sizes, .95), _pct(sizes, .99), sizes[-1])


def _field_stats(records, getter):
    """Null-rate, distinct count, and modal frequency for one field."""
    vals = [getter(r) for r in records]
    n = len(vals)
    non_null = sum(1 for v in vals if v not in ("", None, 0))
    c = Counter(vals)
    _mode, mode_n = c.most_common(1)[0]
    return non_null / n, len(c), mode_n / n


def _norm_attr(s: str) -> str:
    return " ".join(s.casefold().split())


def main() -> None:
    a_records, b_records, groups = load_train(TRAIN)
    pairs = _one_to_one(groups)

    # cardinality distribution
    card = Counter((len(g["A"]), len(g["B"])) for g in groups.values())

    # allocation reconstruction
    alloc_ok = sum(1 for a in a_records
                   if reconstruct_alloc(a.currency, a.value_date, a.account, a.attributes)
                   == a.allocation)

    # direction relation of true 1:1 pairs
    dc = Counter((b.direction, a.direction) for b, a in pairs)

    # amount / date equality of true 1:1 pairs (unsigned magnitude)
    amt_eq = sum(1 for b, a in pairs if b.minor == a.minor)
    date_eq = sum(1 for b, a in pairs if b.value_date == a.value_date)
    both_eq = sum(1 for b, a in pairs if b.minor == a.minor and b.value_date == a.value_date)

    # blocking recall for candidate keys (B-side key -> A index)
    keys = {
        "amount (magnitude)": lambda r: r.minor,
        "(amount, valueDate)": lambda r: (r.minor, r.value_date),
        "(amount, valueDate, ref)": lambda r: (r.minor, r.value_date, r.reference),
        "char-token (attr[:8])": lambda r: _norm_attr(r.attributes)[:8],
    }
    blocking = {name: _blocking(pairs, a_records, kf) for name, kf in keys.items()}

    # per-field null/uniqueness/frequency, both sides
    fields = [
        ("amount", lambda r: r.minor), ("valueDate", lambda r: r.value_date),
        ("reference", lambda r: r.reference), ("attributes", lambda r: r.attributes),
        ("currency", lambda r: r.currency), ("account", lambda r: r.account),
        ("direction", lambda r: r.direction),
    ]
    fstats = {"A": {f: _field_stats(a_records, g) for f, g in fields},
              "B": {f: _field_stats(b_records, g) for f, g in fields}}

    # scope invariants + source/solution invariant (train)
    currencies = {r.currency for r in a_records} | {r.currency for r in b_records}
    accounts = {r.account for r in a_records} | {r.account for r in b_records}
    inv_ok = inv_tot = 0
    for g in groups.values():
        a_allocs = {reconstruct_alloc(a.currency, a.value_date, a.account, a.attributes)
                    for a in g["A"]}
        for b in g["B"]:
            if b.target:
                inv_tot += 1
                inv_ok += b.target in a_allocs

    # unique / ambiguous / impossible on the chosen (amount, valueDate) block
    amt_vd: dict = defaultdict(list)
    for a in a_records:
        amt_vd[(a.minor, a.value_date)].append(a)
    cls = Counter()
    for b, _a in pairs:
        n = len(amt_vd.get((b.minor, b.value_date), []))
        cls["unique" if n == 1 else ("ambiguous" if n > 1 else "impossible")] += 1

    n = len(pairs)
    lines = [
        "# E0 - BenchRec Profiling (train split)",
        "",
        "> Generated by `experiments/benchrec/profile.py`. Train split only; no matching change.",
        "",
        *provenance_lines({"train": TRAIN}, {"scope": "train"}),
        "",
        "## Population",
        f"- Rows: {len(a_records) + len(b_records):,} "
        f"({len(a_records):,} A, {len(b_records):,} B) across {len(groups):,} match groups.",
        f"- A_allocation reconstruction (`cur_valueDate_account_attributes`): "
        f"**{alloc_ok}/{len(a_records)} = {alloc_ok / len(a_records):.2%}** "
        f"-> a predicted A reconstructs its target allocation exactly.",
        "",
        "## Field distributions (non-null / distinct / modal share)",
        "| field | A: non-null | A: distinct | A: mode | B: non-null | B: distinct | B: mode |",
        "|---|---|---|---|---|---|---|",
        *[f"| {f} | {fstats['A'][f][0]:.0%} | {fstats['A'][f][1]:,} | {fstats['A'][f][2]:.0%} "
          f"| {fstats['B'][f][0]:.0%} | {fstats['B'][f][1]:,} | {fstats['B'][f][2]:.0%} |"
          for f, _g in fields],
        "",
        "Notes: `reference` is ~100% populated on **both** sides but A/B values do **not** "
        "correspond (why `(amount,valueDate,ref)` blocking is 0%); `attributes` is the obfuscated "
        "narrative (A ~100%, B ~83%).",
        "",
        "## Scope + source/solution invariants",
        f"- Distinct currencies: {sorted(currencies)} · distinct accounts: {sorted(accounts)} "
        "-> currency/account are **constant** (scope partitioning is a no-op here, but must still "
        "be enforced for correctness/generalization).",
        "- Allocation-conversion invariant: `A_allocation == cur_valueDate_account_attributes` "
        "holds **100%** (above) -> a predicted A always yields the exact target string.",
        f"- Within-group linkage: only **{inv_ok}/{inv_tot} = {inv_ok / inv_tot:.2%}** of matchable "
        "B targets reconstruct from an A **inside the B's own matchId group**. The ~10% gap is "
        "concentrated in multi-record / B-only groups whose target references an A outside the "
        "group -> do NOT assume within-group truth completeness; the eval task scores against the "
        "global solution, not the group. (Characterize this gap by cardinality before E4.)",
        "",
        "## Cardinality (nA, nB) - top groups",
        "| nA:nB | groups |",
        "|---|---|",
        *[f"| {na}:{nb} | {c:,} |" for (na, nb), c in card.most_common(10)],
        "",
        f"1:1 groups = {card[(1, 1)]:,} ({card[(1, 1)] / len(groups):.1%} of groups) - the backbone.",
        "",
        "## Direction of true 1:1 pairs",
        "BenchRec stores unsigned magnitudes; direction is in `debitOrCredit`.",
        "| (B_dir, A_dir) | pairs |",
        "|---|---|",
        *[f"| {bd}->{ad} | {c:,} |" for (bd, ad), c in dc.most_common()],
        "",
        "**True pairs are opposite direction** -> the match key is unsigned magnitude, "
        "and same-sign filtering (as in the current engine tiers 3/4) is wrong here.",
        "",
        "## Equality of true 1:1 pairs",
        f"- amount magnitude equal: **{amt_eq / n:.2%}**",
        f"- valueDate equal: **{date_eq / n:.2%}**",
        f"- both equal: **{both_eq / n:.2%}** "
        f"(the remaining ~{1 - both_eq / n:.1%} is the tolerance/date-drift ceiling)",
        "",
        "## Blocking recall (B-side key indexed over all A) on 1:1 pairs",
        "| block key | recall | size p50 / p95 / p99 / max |",
        "|---|---|---|",
        *[f"| {name} | {rec:.2%} | {p50} / {p95} / {p99} / {mx} |"
          for name, (rec, (p50, p95, p99, mx)) in blocking.items()],
        "",
        "## (amount, valueDate) candidate class for 1:1 B records",
        "| class | count | share |",
        "|---|---|---|",
        *[f"| {k} | {cls[k]:,} | {cls[k] / n:.1%} |"
          for k in ("unique", "ambiguous", "impossible")],
        "",
        "## E0 conclusion",
        "- **Chosen block key for E1: `(amount magnitude, valueDate)`** - "
        f"{blocking['(amount, valueDate)'][0]:.2%} recall with small blocks "
        f"(p99={blocking['(amount, valueDate)'][1][2]}, "
        f"max={blocking['(amount, valueDate)'][1][3]}); amount-alone has equal recall but "
        f"bloated blocks (max={blocking['amount (magnitude)'][1][3]}); "
        "`+ref` is 0% because A-side references are blank.",
        f"- ~{cls['unique'] / n:.0%} of 1:1 B records get a **unique** candidate "
        "(E1's deterministic path); ~{:.0%} are ambiguous (E2 disambiguation); "
        "~{:.0%} impossible (non-exact amount/date, deferred tolerance work)."
        .format(cls['ambiguous'] / n, cls['impossible'] / n),
        "- Direction must be applied as an **opposite-DR/CR** eligibility constraint, "
        "not by signing the amount.",
    ]

    os.makedirs(ART, exist_ok=True)
    out = os.path.join(ART, "e0_profile.md")
    with open(out, "w") as f:
        f.write("\n".join(lines) + "\n")
    print(f"wrote {out}")
    print(f"1:1 pairs={n:,}  block(amount,valueDate) recall="
          f"{blocking['(amount, valueDate)'][0]:.2%}  "
          f"unique={cls['unique'] / n:.0%} ambiguous={cls['ambiguous'] / n:.0%} "
          f"impossible={cls['impossible'] / n:.0%}")


if __name__ == "__main__":
    main()
